#include <stdio.h>
void main() {
    printf("%d\n", 100);            // 100 정수 리터럴
    printf("%f\n", 0.5f);           // 0.500000 정수 리터럴
    printf("%c\n", 'A');            // 'A' 문자 리터럴
    printf("%s\n", "Hello World");  // "Hello World" 문자열 리터럴
    printf("%f\n", 0.1f);           // 0.100000 실수형 리터럴 지수
    printf("%e\n", 1.0e-5f);        // 1.000000e-005 실수형  지수
}